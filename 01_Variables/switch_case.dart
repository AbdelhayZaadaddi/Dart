void main(){
  // Swich Case Statements: Applicable for only 'int' and 'string'
  String grad = 'A';
  switch (grad){
    case 'A':
      print("Execellent grade of A");
    case 'B':
      print('Very good!');
    case 'C':
      print('Good enough but work hard');
      break;
    default:
      print("You have failed");
  }
}